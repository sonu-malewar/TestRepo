package sampleproject;

public class SampleDemo {
	
	public static void main(String[] args) {

		// This is swapping two number program
		
		int a=50,b=30;
		
		a=a+b;  //80
		
		b=a-b;  // 50
		
		a= a-b; //30
		
		System.out.println("a value after swap"+a);
		System.out.println("b value after swap"+b);
		
		
	}

}
